- Use the top 10 highest paying data analyst jobs from first query
- Add the specific skills required for these roles
- Why? It provides a detailed look at which high-paying jobs demand certain skils,
  helping job seekers understand which skills to develop that align with top salaries*/
SELECT*FROM job_postings_fact LIMIT 100;
SELECT*FROM skills_dim LIMIT 100;
SELECT*FROM skills_job_dim LIMIT 100

WITH top_paying_jobs AS (
    SELECT
        job_id,
        job_title,
        salary_year_avg,
        name AS company_name
    FROM    
        job_postings_fact
    LEFT JOIN company_dim
        ON company_dim.company_id = job_postings_fact.company_id
    WHERE   
        job_title_short= 'Data Analyst' AND
        job_location = 'Anywhere' AND
        salary_year_avg is not NULL
    ORDER BY salary_year_avg DESC
    LIMIT 10
)
SELECT 
    top_paying_jobs.*,
    skills_dim.skills
FROM top_paying_jobs
INNER JOIN skills_job_dim
    ON skills_job_dim.  job_id = top_paying_jobs.job_id
LEFT JOIN skills_dim
    ON skills_job_dim.skill_id = skills_dim.skill_id
ORDER BY  
    salary_year_avg DESC;

